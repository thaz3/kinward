import { execFileSync } from "node:child_process";
import { randomBytes } from "node:crypto";
import { existsSync, readFileSync, writeFileSync } from "node:fs";
import { resolve } from "node:path";

const root = resolve(import.meta.dirname, "..");
const envPath = resolve(root, ".env.local");
const status = JSON.parse(
  execFileSync("npx", ["--yes", "supabase@2.109.1", "status", "-o", "json"], {
    cwd: root,
    encoding: "utf8",
  }),
);

const existing = existsSync(envPath) ? readFileSync(envPath, "utf8") : "";
const secretMatch = existing.match(/^KINWARD_AUTH_COOKIE_SECRET=(.+)$/m);
const cookieSecret = secretMatch?.[1] ?? randomBytes(32).toString("base64url");
const local = [
  "# Generated by npm run local:setup. Synthetic local use only.",
  `NEXT_PUBLIC_SUPABASE_URL=${status.API_URL}`,
  `NEXT_PUBLIC_SUPABASE_PUBLISHABLE_KEY=${status.PUBLISHABLE_KEY}`,
  `KINWARD_AUTH_COOKIE_SECRET=${cookieSecret}`,
  "KINWARD_APP_ORIGIN=http://127.0.0.1:3000",
  "KINWARD_SYNTHETIC_INVITATION_SINK=local-only",
  "KINWARD_LOCAL_APP_ORIGIN=http://127.0.0.1:3000",
  "",
].join("\n");

writeFileSync(envPath, local, { mode: 0o600 });
console.log("Local synthetic authentication configured in .env.local.");
console.log("Mailbox: http://127.0.0.1:54324");
